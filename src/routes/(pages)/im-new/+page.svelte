<h3>What happens at the Sunday service?</h3>
<div class="pt-4 pb-2 text-lg">
	We celebrate the Eucharist (also called Holy Communion) in Bonn and Cologne every Sunday. We invite all believing
	Christians to receive Holy Communion. If you do not wish to do so, you are invited to come forward with everyone else
	to receive a blessing in the name of Jesus. In Bonn we stand in a semi-circle around the altar for Holy Communion. In
	Cologne we kneel or stand in a row in front of the altar.
</div>
<div class="py-2 text-lg">
	During the service, we exchange a sign of peace with other people in the congregation. This is usually a handshake,
	but you might see some people giving each other a hug, or simply exchanging a friendly smile or a wave. Just do
	whatever feels most comfortable to you.
</div>
<div class="py-2 text-lg">
	Most services last about 60 minutes, and refreshments are served afterwards. Do stay and say hello; it’ll be great to
	get to know you. If you’d like to be added to the mailing list for regular updates by email, you can fill in your
	contact details on the back of the weekly news sheet and give it to the chaplain.
</div>
<h3>What do I do?</h3>
<div class="pt-4 pb-2 text-lg">
	You can follow the order of service in the green service booklet. You’ll find the readings and the hymn numbers in the
	weekly news sheet, and the hymns in the blue hymn book.
</div>
<div class="py-2 text-lg">You can sit anywhere.</div>
<div class="py-2 text-lg">There’s no dress code.</div>
<div class="py-2 text-lg">During the third hymn there is a collection so you can make a donation if you wish to.</div>
<div class="py-2 text-lg">
	We don’t worry about noisy babies or young children! The church in Cologne has a quiet corner where children can play.
	In Bonn there’s a box of children’s books and colouring pencils at the entrance.
</div>
<h3>How do I get there?</h3>
<div class="pt-4 pb-2 text-lg">
	Look at the section <a href="/locations" target="_blank" class="link">“Where we are”</a> on this website and you will find
	full details, including a map. Both churches have a bus stop very close by.
</div>
<div class="py-2 text-lg">
	If you’re coming by car, you can park outside the church. In Bonn there is a bike rack outside the church where you
	can park your bike.
</div>
<div class="py-2 text-lg">
	Note: Although the name of the Bonn congregation is St. Boniface, the name of the church where we meet is St. Paulus.
</div>
<h3>Accessibility</h3>
<div class="pt-4 pb-2 text-lg">
	Both churches are wheelchair-accessible. Large-print service booklets are available.
</div>
<div class="py-2 text-lg">
	Both churches have a toilet. In Bonn you will need to go up a few steps.
	<div class="py-2 text-lg">
		If you need help with anything, ask one of the churchwardens or the person handing out the books at the entrance.
		They’ll be happy to help.
	</div>
